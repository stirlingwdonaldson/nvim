local function pad_display(text, target)
	local width = vim.fn.strdisplaywidth(text)
	if width >= target then
		return text
	end
	return text .. string.rep(" ", target - width)
end

local function normalize_font(font, rows)
	local normalized = {}
	for char, glyph in pairs(font) do
		local glyph_width = 0
		for row = 1, rows do
			glyph_width = math.max(glyph_width, vim.fn.strdisplaywidth(glyph[row] or ""))
		end
		local normalized_glyph = {}
		for row = 1, rows do
			normalized_glyph[row] = pad_display(glyph[row] or "", glyph_width)
		end
		normalized[char] = normalized_glyph
	end
	return normalized
end

local compact_font_normalized = normalize_font(compact_font, 3)
local regular_font_normalized = normalize_font(regular_font, 5)

local function trim(text)
	return (text:gsub("^%s+", ""):gsub("%s+$", ""))
end

local function rtrim(text)
	return (text:gsub("%s+$", ""))
end

local function starts_with(text, prefix)
	return text:sub(1, #prefix) == prefix
end

local function parse_comment_line(line)
	local indent, content = line:match("^(%s*)(.*)$")
	if content == "" then
		return nil
	end

	local commentstring = vim.bo.commentstring or ""
	if commentstring:find("%%s", 1, true) then
		local raw_prefix, raw_suffix = commentstring:match("^(.*)%%s(.*)$")
		if raw_prefix then
			local prefix = raw_prefix
			local suffix = raw_suffix or ""
			local body = content
			if starts_with(body, prefix) then
				body = body:sub(#prefix + 1)
				if suffix == "" or body:sub(-#suffix) == suffix then
					if suffix ~= "" then
						body = body:sub(1, #body - #suffix)
					end
					return {
						indent = indent,
						prefix = prefix,
						suffix = suffix,
						text = trim(body),
					}
				end
			end
		end
	end

	local fallback_patterns = {
		"^(%-%-%s*)(.*)$",
		"^(//%s*)(.*)$",
		"^(#%s*)(.*)$",
		"^(;%s*)(.*)$",
	}

	for _, pattern in ipairs(fallback_patterns) do
		local prefix, text = content:match(pattern)
		if prefix then
			return {
				indent = indent,
				prefix = prefix,
				suffix = "",
				text = trim(text),
			}
		end
	end

	local block_prefix, block_text, block_suffix = content:match("^(/%*%s*)(.-)(%s*%*/)$")
	if block_prefix then
		return {
			indent = indent,
			prefix = block_prefix,
			suffix = block_suffix,
			text = trim(block_text),
		}
	end

	return nil
end

local function render_title(text, size)
	local font = size == "large" and regular_font_normalized or compact_font_normalized
	local rows = size == "large" and 5 or 3
	local separator = " "
	local out = {}
	for row = 1, rows do
		local parts = {}
		for i = 1, #text do
			local ch = text:sub(i, i):upper()
			local glyph = font[ch] or font[" "]
			parts[#parts + 1] = glyph[row] or ""
		end
		out[row] = rtrim(table.concat(parts, separator))
	end
	return out
end

function M.to_title(size)
	size = size == "large" and "large" or "small"
	local bufnr = vim.api.nvim_get_current_buf()
	local row = vim.api.nvim_win_get_cursor(0)[1]
	local line = vim.api.nvim_buf_get_lines(bufnr, row - 1, row, false)[1]

	local parsed = parse_comment_line(line)
	if not parsed then
		vim.notify("Current line is not a comment", vim.log.levels.WARN)
		return
	end

	if parsed.text == "" then
		vim.notify("Comment line is empty", vim.log.levels.WARN)
		return
	end

	local title_lines = render_title(parsed.text, size)
	local replacement = {}
	for _, title_line in ipairs(title_lines) do
		replacement[#replacement + 1] = parsed.indent .. parsed.prefix .. title_line .. parsed.suffix
	end

	vim.api.nvim_buf_set_lines(bufnr, row - 1, row, false, replacement)
	vim.notify("Comment converted to " .. size .. " title", vim.log.levels.INFO)
end

return M
